CREATE TABLE LEADS (
  LEAD_ID INT(11) PRIMARY KEY,
  NAME VARCHAR(20) NOT NULL,
  CELLPHONE VARCHAR(15) NOT NULL,
  EMAIL VARCHAR(100) NOT NULL,
  MESSAGE TEXT NOT NULL);

