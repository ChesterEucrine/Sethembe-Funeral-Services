CREATE TABLE CLIENTS (
  CLIENT_ID VARCHAR(20) PRIMARY KEY,
  TITLE VARCHAR(10) NOT NULL,
  SURNAME VARCHAR(20) NOT NULL,
  NAME VARCHAR(10) NOT NULL,
  STREET1 VARCHAR(20) NOT NULL,
  STREET2 VARCHAR(20),
  CITY VARCHAR(20) NOT NULL,
  PROVINCE VARCHAR(20) NOT NULL,
  POSTAL VARCHAR(6) NOT NULL,
  F_PLAN VARCHAR(20) NOT NULL,
  C_AMOUNT INT(10) NOT NULL,
  C_MONTH DATE NOT NULL,
  F_MONTH DATE NOT NULL,
  J_DATE DATE NOT NULL,
  CELLPHONE VARCHAR(15) NOT NULL,
  WORKPHONE VARCHAR(15),
  EMAIL VARCHAR(100) NOT NULL,
  ID_PATH VARCHAR(100) NOT NULL);

CREATE TABLE DEPENDENTS (
  CLIENT_ID VARCHAR(20) NOT NULL,
  FOREIGN KEY CLIENT_ID REFERENCES CLIENTS(CLIENT_ID),
  D_NUMBER INT(4) NOT NULL,
  D_ID VARCHAR(20) PRIMARY KEY,
  D_TITLE VARCHAR(10) NOT NULL,
  D_SURNAME VARCHAR(10) NOT NULL,
  D_NAME VARCHAR(20) NOT NULL,
  RELATIONSHIP VARCHAR(10) NOT NULL,
  GENDER VARCHAR(10) NOT NULL);

CREATE TABLE BENEFICIARIES (
  CLIENT_ID VARCHAR(20) NOT NULL,
  FOREIGN KEY CLIENT_ID REFERENCES CLIENTS(CLIENT_ID),
  B_ID VARCHAR(20) PRIMARY KEY,
  B_TITLE VARCHAR(10) NOT NULL,
  B_SURNAME VARCHAR(10) NOT NULL,
  B_NAME VARCHAR(20) NOT NULL);
